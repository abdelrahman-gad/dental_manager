    public function unitTypes(Request $request){
        $unitTypesQuery = UnitType::query();
        if($request->level){
            $unitTypesQuery->where('level',$request->level);
        }
        if($request->direction){
            $unitTypesQuery->where('direction',$request->direction);
        }

        $unitTypes = $unitTypesQuery->get();
        return response()->json([
            'data' => $unitTypes,
            'message'=>''
        ],Response::HTTP_OK);
    }

